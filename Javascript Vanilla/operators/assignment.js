// Assignment operator =

// Assignment operator +=
var x, y;
x += y; // this
x = x + y; // is the same as this

// Assignment operator -=
x -= y; // this
x = x - y; // is the same as this

// Assignment operator *=
x *= y; // this
x = x * y; // is the same as this

// Assignment operator /=
x /= y; // this
x = x / y; // is the same as this

// Assignment operator %=
x %= y; // this
x = x % y; // is the same as this

// Assignment operator **=
x **= y; // this
x = x ** y; // is the same as this