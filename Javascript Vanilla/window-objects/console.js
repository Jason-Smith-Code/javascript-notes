// console.assert() : Writes an error message to the console if a assertion is false

// console.clear() : wipes the console

// console.count() : Logs the number of times that this particular call to count() has been called

// console.error() : Outputs an error message to the console

// console.group() : Creates a new inline group in the console. This indents following console messages by an additional level, until console.groupEnd() is called

// console.groupCollapse() : Creates a new inline group in the console. However, the new group is created collapsed. The user will need to use the disclosure button to expand it

// console.groupEnd() : Exits the current inline group in the console

// console.info() : Outputs an informational message to the console

// console.log() : Outputs a message to the console

// console.table() : Displays tabular data as a table

// console.time() : Displays tabular data as a table

// console.timeEnd() : Stops a timer that was previously started by console.time()

// console.trace() : Outputs a stack trace to the console

// console.warn() : Outputs a warning message to the console
