// Bitwise work on 32 bits numbers

// Bitwise operator and : &
console.log(`15 & 2: ${15 & 2}`);
console.log(`2 & 15: ${2 & 15}`);

// Bitwise operator or : |
console.log(`15 | 2: ${15 | 2}`);
console.log(`2 | 15: ${2 | 15}`);

// Bitwise operator not : ~

// Bitwise operator xor : ^
console.log(`15 ^ 2: ${15 ^ 2}`);
console.log(`2 ^ 15: ${2 ^ 15}`);

// Bitwise operator left shift: <<
console.log(`15 >> 2: ${15 >> 2}`);
console.log(`2 >> 15: ${2 >> 15}`);

// Bitwise operator right shift: >>
console.log(`15 << 2: ${15 << 2}`);
console.log(`2 << 15: ${2 << 15}`);