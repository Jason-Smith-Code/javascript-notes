// Comparison operator == is equal to (compares value)
console.log(`1 == "1" : ${1 == "1"}`);
console.log(`null == null : ${null == null}`);
console.log(`undefined == undefined : ${undefined == undefined}`);
console.log(`{} == {} : ${{} == {}}`);

// Comparison operator === is strict equal to ( compares value and type)
console.log(`1 === "1" : ${1 === "1"}`);
console.log(`null === null : ${null === null}`);
console.log(`undefined === undefined : ${undefined === undefined}`);
console.log(`{} === {} : ${{} === {}}`);

