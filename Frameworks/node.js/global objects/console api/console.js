// console.log() : black colour text with no icon
console.log();

// console.count()
console.count("string");
// returns the number of times the string has been run in the console.

// console.clear() : wipes the console log.

// console.warn("warn") : Yellow color text with icon (in chrome browser)
console.warn("warn");

// console.info("info") : blue color text with icon (in chrome browser)
console.info("info");

// console.info("error") : red color text with icon (in chrome browser)
console.info("error");

// console.table() : prints the content in a table format in the console.
console.table([
  { a: 1, b: 2 },
  { a: 5, b: 5 },
]);

// console.debug() :
console.debug();
