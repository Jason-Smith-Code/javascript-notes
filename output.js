// There are 4 ways to output Javascript
// using innerhtml to add content to the document
// using .write to add content to the document
// using window alert create a browser window pop up
// and writing to the console

// innerHTML
document.getElementById("inner-html").innerHTML = 5 + 6;

// document.write
document.write("document.write is inserted where the script goes");
// Using document.write() after an HTML document is loaded, will delete all existing HTML and render the contents of the .write method only.

// window.alert
window.alert(5 + 6);

// console log
console.log("message goes here");
